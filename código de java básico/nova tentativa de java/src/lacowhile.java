public class lacowhile {
    // código do laço while aqui...
    // incrementa o contador em cada iteração do laço
    // imprime o contador na tela e avança para a próxima iteração do laço
    // usar o break para sair do laço após imprimir a mensagem correspondente ao caso escolhido
    
public static void main(String[] args) {
    int contador = 1;

    // laço enquanto contador for menor ou igual a 100
    // incrementa o contador em cada iteração do laço
    // imprime o contador na tela e avança para a próxima iteração do laço
    while (contador <= 100) {
        System.out.println("contador: " + contador);
        contador++;
    }
}
}
